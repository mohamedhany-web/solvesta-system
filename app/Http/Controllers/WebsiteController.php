    public function caseStudyShow(string $slug)
    {
        $case = collect($this->caseStudiesData())->firstWhere('slug', $slug);
        abort_unless($case, 404);

        return view('website.case-studies.show', [
            'case' => $case,
            'caseStudies' => $this->caseStudiesData(),
        ]);
    }
